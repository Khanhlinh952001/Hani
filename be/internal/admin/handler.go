package admin

import (
	"net/http"
	"strconv"

	"github.com/gin-gonic/gin"
)

func StatsHandler(c *gin.Context) {
	stats, err := GetStatsService()
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}
	c.JSON(http.StatusOK, stats)
}

